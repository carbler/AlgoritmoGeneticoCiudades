
package Nodos;

/**
 * Esta clase contiene las variables de cada vertice
 * @author erley
 */
public class Vertices {
    
    private int inicio;
    private int fin;
    private int distancia;
    private double tiempo;
    private int peajes;
    private int estado_via;
    private int tipo_via;
    private int condiciones_via;
    private int seguridad_via;

    /**
     * @return the inicio
     */
    public int getInicio() {
        return inicio;
    }

    /**
     * @param inicio the inicio to set
     */
    public void setInicio(int inicio) {
        this.inicio = inicio;
    }

    /**
     * @return the fin
     */
    public int getFin() {
        return fin;
    }

    /**
     * @param fin the fin to set
     */
    public void setFin(int fin) {
        this.fin = fin;
    }

    /**
     * @return the distancia
     */
    public int getDistancia() {
        return distancia;
    }

    /**
     * @param distancia the distancia to set
     */
    public void setDistancia(int distancia) {
        this.distancia = distancia;
    }

    /**
     * @return the tiempo
     */
    public double getTiempo() {
        return tiempo;
    }

    /**
     * @param tiempo the tiempo to set
     */
    public void setTiempo(double tiempo) {
        this.tiempo = tiempo;
    }

    /**
     * @return the peajes
     */
    public int getPeajes() {
        return peajes;
    }

    /**
     * @param peajes the peajes to set
     */
    public void setPeajes(int peajes) {
        this.peajes = peajes;
    }

    /**
     * @return the estado_via
     */
    public int getEstado_via() {
        return estado_via;
    }

    /**
     * @param estado_via the estado_via to set
     */
    public void setEstado_via(int estado_via) {
        this.estado_via = estado_via;
    }

    /**
     * @return the tipo_via
     */
    public int getTipo_via() {
        return tipo_via;
    }

    /**
     * @param tipo_via the tipo_via to set
     */
    public void setTipo_via(int tipo_via) {
        this.tipo_via = tipo_via;
    }

    /**
     * @return the condiciones_via
     */
    public int getCondiciones_via() {
        return condiciones_via;
    }

    /**
     * @param condiciones_via the condiciones_via to set
     */
    public void setCondiciones_via(int condiciones_via) {
        this.condiciones_via = condiciones_via;
    }

    /**
     * @return the seguridad_via
     */
    public int getSeguridad_via() {
        return seguridad_via;
    }

    /**
     * @param seguridad_via the seguridad_via to set
     */
    public void setSeguridad_via(int seguridad_via) {
        this.seguridad_via = seguridad_via;
    }
   
    
    
}
